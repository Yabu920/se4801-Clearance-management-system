CREATE TABLE clearance_requests (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    request_type VARCHAR(30) NOT NULL,
    status VARCHAR(40) NOT NULL,
    reason TEXT,
    student_profile_id BIGINT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_clearance_requests_student_profile FOREIGN KEY (student_profile_id) REFERENCES student_profiles (id),
    CONSTRAINT chk_clearance_requests_type CHECK (request_type IN ('GRADUATION', 'WITHDRAWAL', 'TRANSFER')),
    CONSTRAINT chk_clearance_requests_status CHECK (status IN ('PENDING', 'IN_REVIEW', 'READY_FOR_REGISTRAR', 'COMPLETED', 'REJECTED', 'CANCELLED'))
);
